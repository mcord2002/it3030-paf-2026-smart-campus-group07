package com.campus.hub.domain;

// Enum representing different types of resources available in the system
public enum ResourceType {

	// Large space used for lectures or classes
	LECTURE_HALL,

	// Laboratory used for practical sessions or experiments
	LAB,

	// Small room used for meetings or discussions
	MEETING_ROOM,

	// Movable items such as projectors, laptops, etc.
	EQUIPMENT,

	// Any other type of resource not covered above
	OTHER
}